            <h3 class="section-title">Apa itu ARCSPIN?</h3>
            <div class="section-body mb-4">
                <p>ARCSPIN merupakan aplikasi spinner artikel membantu Anda membuat konten unik.</p>
                <p>Anda dapat menggunakan ARCSPIN untuk membantu dalam menulis konten unik dalam waktu singkat, termasuk menulis blog, feed, atau apapun yang ingin Anda tulis.</p>
            </div>
        </div>

        <div class="section-block mt-4">
            <h3 class="section-title">Apa itu spintax?</h3>
            <div class="section-body mb-4">
                <p>
                    Spintax merupakan format kata yang dapat digunakan dalam mengacak kata.
                    Spintax diawali dengan tanda kurung awal '{' dan diakhiri dengan kurung kurawal '}' kemudian tiap kata atau kalimat dipisahkan dengan pipeline '|'.
                    Spintax 1 mempunyai kata orisinil sedangkan Spintax 2 tidak.
                </p>
                <p>
                    Contohnya, 'menulis' mempunyai sinonim mencatat dan menyalin. Spintax 1 dan Spintax 2 yang dapat dihasilkan adalah {mencatat|menyalin|menulis} dan {mencatat|menyalin}.
                <p>
                    Cobalah "Aku {mencatat|menyalin|menulis} {artikel|tulisan}.", lalu tekan "Spin input". Anda akan mendapatkan banyak variasi hasil spinning jika menekannya berulang kali.
                </p>
            </div>
        </div>

        <div class="section-block mt-4">
            <h3 class="section-title">Bagaimana cara ARCSPIN bekerja?</h3>
            <div class="section-body mb-4">
                <p>
                    ARCSPIN dapat mengacak artikel dalam format spintax dengan cepat.
                    ARCSPIN juga dapat menghasilkan spintax dengan memanfaatkan sinonim kata.
                    Jika Anda mempunyai artikel dengan format spintax, langsung saja menaruhnya di kotak input dan tekan "Spin input"
                </p>

            </div>
        </div>

        <div class="section-block mt-4">
            <h3 class="section-title">Bagaimana cara menghasilkan artikel unik menggunakan ARCSPIN?</h3>
            <div class="section-body mb-4">
                <p>
                    Jika Anda mempunyai artikel dengan format spintax, langsung saja menaruhnya di kotak input dan tekan "Spin input".
                </p>
                <p>
                    Jika Anda tidak mempunyai artikel dengan format spintax, Anda dapat ARCSPIN menghasilkan artikel dengan format spintax sesuai dengan jenisnya, yaitu 'Spintax 1' atau 'Spintax 2'. Setelah itu, ubah spintax sesuai yang Anda inginkan lalu tekan 'Spin output'.
                </p>
            </div>
        </div>
    </div>
